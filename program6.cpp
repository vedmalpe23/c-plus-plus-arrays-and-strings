//Concatenation of two strings
#include<iostream>
using namespace std;
int main()
{
    string a,b;
    cout<<"Enter the string a: ";
    cin>>a;
    cout<<"Enter the string b: ";
    cin>>b;
    cout<<"Concatenation of the strings: "<<a+b;
    return 0;
}
/*output:
Enter the string a: Tarushi
Enter the string b: Verma
Concatenation of the strings: TarushiVerma
*/
